-- ○ PK NOT NULL 조건 때문에 CSV IMPORT가 안됨
-- ○ TEMP 테이블에 만들고 삽입
CREATE TABLE CONTENT_TEMP
(
    ADDR1         VARCHAR2(1000),
    ADDR2         VARCHAR2(1000),
    CONTENTTYPEID VARCHAR2(1000),
    CREATEDTIME   VARCHAR2(14),
    MAPX          VARCHAR2(50),   -- NUMBER → VARCHAR2
    MAPY          VARCHAR2(50),   -- NUMBER → VARCHAR2
    MODIFIEDTIME  VARCHAR2(14),
    TITLE         VARCHAR2(500),
    APIID         VARCHAR2(1000)
);

-- ○ TEMP에 있는 데이터들을 CONTENT TABLE 에 옮기는 작업.
-- 1. TEMP에 CSV IMPORT
-- 2. TEMP 데이터 CONTENT 테이블에 옮기기
-- 3. 확인하기
INSERT INTO CONTENT (CONTENTID, ADDR1, ADDR2, CONTENTTYPEID, CREATEDTIME, MAPX, MAPY, MODIFIEDTIME, TITLE, APIID)
SELECT SEQ_CONTENTID.NEXTVAL, ADDR1, ADDR2, CONTENTTYPEID, CREATEDTIME,
       NULLIF(MAPX, ''), NULLIF(MAPY, ''),  -- 빈 문자열을 NULL로 변환
       MODIFIEDTIME, TITLE, APIID
FROM CONTENT_TEMP;

