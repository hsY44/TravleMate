-- 컨텐츠 카테고리 테이블
CREATE TABLE CONTENTCATEGORY
(
    CONTENTTYPEID  VARCHAR2(1000) PRIMARY KEY,  -- 컨텐츠카테고리코드
    CONTENTDIV   VARCHAR2(100)                -- 컨텐츠구분 (12:관광지, 14:문화시설, 15:축제공연행사, 25:여행코스, 28:레포츠, 32:숙박, 38:쇼핑, 39:음식점)
);

-- ○ 컨텐츠 테이블
CREATE TABLE CONTENT
(
    CONTENTID     NUMBER        NOT NULL,     -- 컨텐츠고유번호
    TITLE         VARCHAR2(500),              -- 컨텐츠명
    ADDR1         VARCHAR2(1000),             -- 주소
    ADDR2         VARCHAR2(1000),             -- 상세주소
    CONTENTTYPEID VARCHAR2(1000),             -- 컨텐츠구분코드
    CREATEDTIME   VARCHAR2(14),               -- 생성일자
    MAPX          NUMBER(20, 15),             -- 위도
    MAPY          NUMBER(20, 15),             -- 경도
    MODIFIEDTIME  VARCHAR2(14),               -- 수정일자
    APIID         VARCHAR2(1000),             -- API컨텐츠아이디

    CONSTRAINT PK_CONTENTID     PRIMARY KEY (CONTENTID),
    CONSTRAINT FK_CONTENTTYPEID FOREIGN KEY (CONTENTTYPEID)
        REFERENCES CONTENTCATEGORY (CONTENTTYPEID)
);

-- ○ 이미지 테이블
CREATE TABLE CONTENTIMAGE(
    CONTENTIMAGEID      NUMBER        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
    , APIID VARCHAR2(100)             NOT NULL
    , ORIGINIMGURL VARCHAR2(1000)
    , IMGNAME VARCHAR2(500)
    , CPYRHTDIVCD VARCHAR2(100)
    , SERIALNUM VARCHAR2(500)
    , CONSTRAINT FK_CONTENTIMAGE_APIID FOREIGN KEY (APIID)
        REFERENCES CONTENT (APIID)
);

