START WITH 1
INCREMENT BY 1
NOCACHE;

-- ○ 옮기기 위해 IMAGE_TEMP 테이블 만듦.
CREATE TABLE IMAGE_TEMP
(
    APIID       VARCHAR2(1000),
    FIRSTIMAGE  VARCHAR2(1000),
    FIRSTIMAGE2 VARCHAR2(1000)
);

-- TEMP에 있는 데이터를 IMAGE 테이블에 옮기는 과정
-- ○ FIRSTIMAGE
INSERT INTO IMAGE (CONTENTIMAGEID, CONTENTID, IMAGE)
SELECT SEQ_CONTENTIMAGEID.NEXTVAL, C.CONTENTID, T.FIRSTIMAGE
FROM IMAGE_TEMP T
JOIN CONTENT C ON C.APIID = T.APIID
WHERE T.FIRSTIMAGE IS NOT NULL;

-- ○ FIRSTIMAGE2
INSERT INTO IMAGE (CONTENTIMAGEID, CONTENTID, IMAGE)
SELECT SEQ_CONTENTIMAGEID.NEXTVAL, C.CONTENTID, T.FIRSTIMAGE2
FROM IMAGE_TEMP T
JOIN CONTENT C ON C.APIID = T.APIID
WHERE T.FIRSTIMAGE2 IS NOT NULL;


